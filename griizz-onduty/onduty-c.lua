RegisterCommand("onduty", function()
    giveWeapon("weapon_nightstick")
    giveWeapon("weapon_stungun")
    giveWeapon("weapon_flashlight")
    giveWeapon("weapon_combatpistol")
    GiveWeaponComponentToPed(GetPlayerPed(-1), 1593441988, 0x359B7AAE)
    giveWeapon("weapon_carbinerifle")
    GiveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0x7BC4CDDC)
    GiveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0xC164F53)
    GiveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0xA0D89C42)
    giveWeapon("weapon_pumpshotgun")
    GiveWeaponComponentToPed(GetPlayerPed(-1), 487013001, 0x7BC4CDDC)
    alert("~b~Given Cop Weapon Loadout")
end)

RegisterCommand("clearw", function()
    RemoveAllPedWeapons(GetPlayerPed(-1), true)
    notify('~r~Cleared All Weapons.')
end)

RegisterCommand("offduty", function()
    removeWeapon("weapon_nightstick")
    removeWeapon("weapon_stungun")
    removeWeapon("weapon_flashlight")
    giveWeapon("weapon_combatpistol")
    RemoveWeaponComponentToPed(GetPlayerPed(-1), 1593441988, 0x359B7AAE)
    removeWeapon("weapon_carbinerifle")
    RemoveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0x7BC4CDDC)
    RemoveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0xC164F53)
    RemoveWeaponComponentToPed(GetPlayerPed(-1), -2084633992, 0xA0D89C42)
    removeWeapon("weapon_pumpshotgun")
    RemoveWeaponComponentToPed(GetPlayerPed(-1), 487013001, 0x7BC4CDDC)
    alert("~b~Removed Loadout")
end)